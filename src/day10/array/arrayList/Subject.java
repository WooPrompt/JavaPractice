package day10.array.arrayList;

public class Subject {
    String subjectName;
    private int score;
    private char grade;

    public char getGrade() {
        return grade;
    }

    public void setGrade(char grade) {
        this.grade = grade;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public int getScore() {
        return score;
    }

    public String getSubjectName() {
        return subjectName;
    }
}
